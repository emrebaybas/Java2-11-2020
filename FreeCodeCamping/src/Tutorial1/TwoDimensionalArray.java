package Tutorial1;

public class TwoDimensionalArray {

	public static void main(String[] args) {
		
		int [][] numberGrid= {
				
				{1,2,3},
				{4,5,6},
				{7,8,9},
				{0},
		};
		
		System.out.println(numberGrid[0][0]);
		System.out.println(numberGrid[0][1]);
		System.out.println(numberGrid[0][2]);

		System.out.println(numberGrid[1][0]);
		System.out.println(numberGrid[1][1]);
		System.out.println(numberGrid[1][2]);
		
		System.out.println(numberGrid[2][0]);
		System.out.println(numberGrid[2][1]);
		System.out.println(numberGrid[2][2]);
		
		System.out.println(numberGrid[3][0]);
	}

}
